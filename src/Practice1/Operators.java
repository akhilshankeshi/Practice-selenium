package Practice1;

public class Operators {
public static void main(String[] args) {
	//Arithmetic Operator
	
	int a=50;
	int b=20;
	
	System.out.println(a+b);
	System.out.println(a-b);
	System.out.println(a*b);
	System.out.println(a/b);
	System.out.println(a%b);
	
	
	//Comparison Operator
	
	int i=15;
	int j=20;
	
	System.out.println();
	System.out.println(i>j);
	System.out.println(i<j);
	System.out.println(i==j);
	System.out.println(i>=j);
	System.out.println(i<=j);
	System.out.println(i!=j);
}

}
